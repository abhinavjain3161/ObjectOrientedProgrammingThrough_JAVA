class Example3
{
	public static void main(String[] args)
	{
		int var1 = 10;
		int var2 = 5;
		System.out.println("var1 = "+var1);
		System.out.println("var2 = "+var2);
		System.out.println("var1 + var2 = ");
		System.out.println(var1 + var2);
		System.out.println("var1 - var2 = ");
		System.out.println(var1-var2);
		System.out.println("var1 * var2 = ");
		System.out.println(var1*var2);
		System.out.println("var1 / var2 = ");
		System.out.println(var1/var2);
	}
}
