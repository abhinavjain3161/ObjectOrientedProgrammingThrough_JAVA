//3.Write a Java program to demonstrate the use of primitive data types.
// 8 types of primitive data types
class pdtypes
{
	public static void main(String[] args)
	{
		int n = 45; // Integer
		System.out.println("Integer: "+ n);
		float p = 34.8f;
		System.out.println("Floating: " + p);
		char ch = 'A';
		System.out.println("Character: " + ch);
		byte b  = 8;
		System.out.println("Byte: " + b);
		short s = 8980;
		System.out.println("Short: " + s);
		long l = 18765687;
		System.out.println("Long: " + l);
		boolean f = true;
		System.out.println("Boolean : " + f);
		double d = 45.3;
		System.out.println("Double: " + d);
	}
}
		